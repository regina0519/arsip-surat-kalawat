    private void setState(){
        if(state==Global.STATE_ADD)this.adding=true;
        else this.adding=true;
        
        if(state==Global.STATE_ADD){
            this.editMode=true;
            this.btnAdd.setEnabled(false);
            this.btnDel.setEnabled(false);
            this.btnSave.setEnabled(true);
            this.btnEdit.setEnabled(false);
            this.btnCancel.setEnabled(true);
        }else if(state==Global.STATE_DEL){
            this.editMode=false;
            this.btnAdd.setEnabled(false);
            this.btnDel.setEnabled(false);
            this.btnSave.setEnabled(false);
            this.btnEdit.setEnabled(true);
            this.btnCancel.setEnabled(false);
        }else if(state==Global.STATE_INIT){
            this.editMode=false;
            this.btnAdd.setEnabled(false);
            this.btnDel.setEnabled(false);
            this.btnSave.setEnabled(false);
            this.btnEdit.setEnabled(true);
            this.btnCancel.setEnabled(false);
        }else if(state==Global.STATE_CANCEL){
            this.editMode=false;
            this.btnAdd.setEnabled(false);
            this.btnDel.setEnabled(false);
            this.btnSave.setEnabled(false);
            this.btnEdit.setEnabled(true);
            this.btnCancel.setEnabled(false);
        }else if(state==Global.STATE_SAVE){
            this.editMode=false;
            this.btnAdd.setEnabled(false);
            this.btnDel.setEnabled(false);
            this.btnSave.setEnabled(false);
            this.btnEdit.setEnabled(true);
            this.btnCancel.setEnabled(false);
        }else if(state==Global.STATE_EDIT){
            this.editMode=true;
            this.btnAdd.setEnabled(true);
            this.btnDel.setEnabled(true);
            this.btnSave.setEnabled(true);
            this.btnEdit.setEnabled(false);
            this.btnCancel.setEnabled(true);
        }
        fetchData();
        setLock();
    }
    private void fetchData(){
        if(this.tbl==null)return;
        
        if(this.state==Global.STATE_ADD){
            
            this.jTextField1.setText(this.tbl.getCurrentRow().getCells().get(0).getText());
            this.jTextField2.setText("");
            this.jTextField3.setText("");
            this.jTextField4.setText("now");
            this.jTextField5.setText("");
            this.jTextField6.setText("");
            this.jTextField7.setText("");
            this.jTextField8.setText("");
            this.jCheckBox1.setSelected(false);
            this.jTextField10.setText("Camat Kalawat");
            this.jTextField11.setText("");
        }else{
            this.jTextField1.setText(this.tbl.getCurrentRow().getCells().get(0).getText());
            this.jTextField2.setText(this.tbl.getCurrentRow().getCells().get(1).getText());
            this.jTextField3.setText(this.tbl.getCurrentRow().getCells().get(2).getText());
            this.jTextField4.setText(this.tbl.getCurrentRow().getCells().get(3).getText());
            this.jTextField5.setText(this.tbl.getCurrentRow().getCells().get(4).getText());
            this.jTextField6.setText(this.tbl.getCurrentRow().getCells().get(5).getText());
            this.jTextField7.setText(this.tbl.getCurrentRow().getCells().get(6).getText());
            this.jTextField8.setText(this.tbl.getCurrentRow().getCells().get(7).getText());
            this.jCheckBox1.setSelected(this.tbl.getCurrentRow().getCells().get(9).getValueBoolean());
            this.jTextField10.setText(this.tbl.getCurrentRow().getCells().get(8).getText());
            this.jTextField11.setText(this.tbl.getCurrentRow().getCells().get(10).getText());
        }
        
    }
